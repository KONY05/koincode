#!/usr/bin/env bun
/**
 * Regenerates src/lib/ide-extension-assets.ts from the compiled
 * packages/vscode-extension build output. Source of truth is
 * packages/vscode-extension/src/extension.ts — this file's embedded
 * EXTENSION_JS/EXTENSION_PACKAGE_JSON/EXTENSION_VERSION constants must never
 * be hand-edited, since they're what actually gets extracted to disk and
 * installed by lib/ide-extension.ts (not packages/vscode-extension/out/
 * itself — see that file's header comment for why: it needs to work
 * identically across compiled binaries, npm installs, and curl/iex installs,
 * so it can't have a runtime filesystem dependency on a sibling package).
 *
 * Runs automatically as part of `bun run build` (bin/build.sh) and
 * `bun run compile` (bin/compile.ts). Run manually via
 * `bun run sync:ide-extension` (from repo root) after editing extension.ts.
 */

import { execFileSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";

const CLI_DIR = path.join(import.meta.dir, "..");
const EXT_DIR = path.join(CLI_DIR, "..", "vscode-extension");
const OUT_FILE = path.join(CLI_DIR, "src", "lib", "ide-extension-assets.ts");

// Always rebuild first so this reflects current source, not a stale out/.
execFileSync("bun", ["run", "build"], { cwd: EXT_DIR, stdio: "inherit" });

const pkg = JSON.parse(readFileSync(path.join(EXT_DIR, "package.json"), "utf8"));
const extensionJs = readFileSync(path.join(EXT_DIR, "out", "extension.js"), "utf8");

// The extension.js body becomes a backtick string below, so its own
// backslashes/backticks/${...} need neutralizing first.
function escapeForTemplateLiteral(s: string): string {
  return s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

const output = `/**
 * AUTO-GENERATED by bin/sync-ide-extension.ts — do not hand-edit.
 * Source of truth is packages/vscode-extension/src/extension.ts. Regenerate
 * with \`bun run sync:ide-extension\` (also runs automatically as part of the
 * CLI build/compile scripts).
 *
 * Embedded as string constants (not read from packages/vscode-extension/out/
 * at runtime) so extraction works identically across compiled binaries, npm
 * installs, and curl/iex installs, with no filesystem dependency on a
 * sibling package.
 */

export const EXTENSION_VERSION = ${JSON.stringify(pkg.version)};

export const EXTENSION_PACKAGE_JSON = JSON.stringify(
  {
    name: ${JSON.stringify(pkg.name)},
    displayName: ${JSON.stringify(pkg.displayName)},
    description: ${JSON.stringify(pkg.description)},
    version: EXTENSION_VERSION,
    engines: ${JSON.stringify(pkg.engines)},
    categories: ${JSON.stringify(pkg.categories)},
    activationEvents: ${JSON.stringify(pkg.activationEvents)},
    main: ${JSON.stringify(pkg.main)},
    contributes: ${JSON.stringify(pkg.contributes)},
  },
  null,
  2,
);

export const EXTENSION_JS = \`${escapeForTemplateLiteral(extensionJs)}\`;
`;

writeFileSync(OUT_FILE, output);
console.log(`Wrote ${path.relative(path.join(CLI_DIR, ".."), OUT_FILE)} (extension v${pkg.version})`);
